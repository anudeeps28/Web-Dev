// getters and setters for web-development
let img = document.querySelector('img');
// getAttribute
img.getAttribute('id'); // mainImg

// setAttribute
img.setAttribute('id', 'spidermanImage') // ID of spiderman image is set to spidermanImage

// another example
img.getAttribute('class'); // null because not defined
img.setAttribute('class', 'images'); // now the image has a class called 'images'


// similarly we can get and set any attribute from our html file
