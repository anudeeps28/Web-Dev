let size = "XL";

if (size == "XL") {
    console.log("The price is Rs. 250");
} else if(size == "L") {
    console.log("The price is Rs. 200");
} else if(size == "M") {
    console.log("The price is Rs. 100");
} else if(size == "S") {
    console.log("The price is Rs. 50");
} else {
    console.log("Wrong size entered!")
}