let i=1;
// forward
while(i<=5) {
    console.log(i);
    i++;
}

console.log("///////////////")

// backwards
let i3 = 10;
while(i3 >=0) {
    console.log(i3);
    i3--;
}

console.log("///////////////")

// steps of 2
let i2 = 0;
while (i2<=20) {
    console.log(i2);
    i2 += 2;
}

console.log("///////////////")